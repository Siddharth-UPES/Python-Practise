#Take the input from the user
name=input("Enter your name: ")

#print the name in the uppercase
print("Uppercase:",name.upper())

#print the name as lowercase
print ("Lowercase:",name.lower())

#print the length of the name
print("Length of the name:",len(name))

#check if the name start with a specific letter
print("Does your name start with 'A'? :",name.startswith('a'))

start_with_a=name.startwith('a')
print("Does your name start with 'A'? :",start_with_a)

